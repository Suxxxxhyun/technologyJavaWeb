package sec01.ex02;

import java.io.IOException;
import java.io.PrintWriter;

import javax.servlet.ServletConfig;
import javax.servlet.ServletException;
import javax.servlet.annotation.WebServlet;
import javax.servlet.http.HttpServlet;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;

/**
 * Servlet implementation class SecondServlet
 */
@WebServlet("/second")
public class SecondServlet extends HttpServlet {

	public void init(ServletConfig config) throws ServletException {
		System.out.println("init 메서드 호출");
	}

	/**
	 * @see Servlet#destroy()
	 */
	public void destroy() {
		System.out.println("destroy 메서드 호출");
	}

	/**
	 * @see HttpServlet#doGet(HttpServletRequest request, HttpServletResponse response)
	 */
	protected void doGet(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
		request.setCharacterEncoding("utf-8");
		response.setContentType("text/html; charset=utf-8");
		PrintWriter out = response.getWriter();
		
		String user_id = request.getParameter("user_id");
		String user_pw = request.getParameter("user_pw");
		String user_address = request.getParameter("user_address");
		String user_email = request.getParameter("user_email");
		//String user_hp = request.getParameter("user_hp");
		
		out.print("<html><body>");
		if(user_id!=null & user_id.length()!=0) {
			out.print("이미 로그인 상태입니다!");
			out.print("첫 번째 서블릿에서 넘겨준 아이디 : " + user_id);
			out.print("두 번째 서블릿에서 넘겨준 비밀번호 : " + user_pw);
			out.print("세 번째 서블릿에서 넘겨준 주소 : " + user_address );
			out.print("</body></html>");
		}else {
			out.print("로그인 하지 않았습니다.<br><br>");
			out.print("다시 로그인하라<br>");
			out.print("<a href='/pro09/login.html'>로그인 창으로 이동하기</a>");
		}
		
		System.out.println(user_address);
		
		
	}

}
